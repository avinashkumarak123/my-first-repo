#include <iostream>
using namespace std;

int main() {
    int num1, num2, result;

    // Input two numbers
    cout << "Enter first number: ";
    cin >> num1;
    cout << "Enter second number: ";
    cin >> num2;

    // Subtract the numbers
    result = num1 - num2;

    // Display the result
    cout << "The result is: " << result << endl;

    return 0;
}